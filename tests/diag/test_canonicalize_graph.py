# ── Layer 3: edge cases that stress-test canonicalize_contraction_graph ───────

class TestEdgeCasesOV:
    """Edge cases for the two-space (occupied/virtual) setup."""

    @pytest.fixture(autouse=True)
    def _setup(self):
        setup_ov()

    # ── rank constraints ───────────────────────────────────────────────────────

    def test_rank_zero_terms_are_fully_contracted(self):
        """Contracting to rank 0 must yield only scalar (fully contracted) terms."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        wt = w.WickTheorem(); wt.set_single_threaded(True)
        result = wt.contract(w.rational(1), V@T2, 0, 0)
        assert len(result) > 0, "V@T2 rank-0 contraction should be non-empty"
        for term, _ in result:
            s = str(term)
            assert "a+" not in s and "a-" not in s, (
                f"rank-0 term still has free SQ operators: {s}"
            )

    def test_rank_filter_strictly_reduces_terms(self):
        """Restricting maxrank must give at most as many terms as the full contraction."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        wt_full = w.WickTheorem(); wt_full.set_single_threaded(True)
        wt_r2   = w.WickTheorem(); wt_r2.set_single_threaded(True)
        full = wt_full.contract(w.rational(1), V@T2, 0, 999)
        r2   = wt_r2.contract(w.rational(1), V@T2,   0, 2)
        assert len(r2) <= len(full)

    def test_rank_one_result_contains_only_one_body_operators(self):
        """Contracting to rank 1 must yield only 1-body residual terms."""
        F  = w.utils.gen_op("f", 1, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        wt = w.WickTheorem(); wt.set_single_threaded(True)
        result = wt.contract(w.rational(1), F@T2, 1, 1)
        for term, _ in result:
            s = str(term)
            n_cre = s.count("a+(")
            n_ann = s.count("a-(")
            assert n_cre == 1 and n_ann == 1, (
                f"rank-1 term has wrong operator count: cre={n_cre} ann={n_ann}"
            )

    # ── determinism ───────────────────────────────────────────────────────────

    def test_canon_is_deterministic(self):
        """Contracting the same expression twice must return equal results."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T3 = w.op("t", ["v+ v+ v+ o o o"])
        result1 = contract(V@T3@T3, canon=True)
        result2 = contract(V@T3@T3, canon=True)
        assert result1 == result2

    def test_nocanon_is_deterministic(self):
        """No-canon mode must also be deterministic."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T3 = w.op("t", ["v+ v+ v+ o o o"])
        result1 = contract(V@T3@T3, canon=False)
        result2 = contract(V@T3@T3, canon=False)
        assert result1 == result2

    # ── one-body simplicity ───────────────────────────────────────────────────

    def test_one_body_canon_equals_nocanon(self):
        """For a single one-body operator product, graph canonicalization
        cannot change the physical content: canon=ON and canon=OFF should
        give the same expression (only one operator permutation is possible)."""
        F  = w.utils.gen_op("f", 1, "ov", "ov")
        T1 = w.op("t", ["v+ o"])
        r_on  = contract(F@T1, canon=True)
        r_off = contract(F@T1, canon=False)
        assert r_on == r_off

    # ── non-contracting cases ─────────────────────────────────────────────────

    def test_no_rank_zero_for_unmatched_operators(self):
        """An expression that cannot produce a vacuum expectation value
        should have no rank-0 terms (particle-number non-conserving)."""
        T1 = w.op("t", ["v+ o"])   # only creation-type in SQ sense
        T2 = w.op("t", ["v+ v+ o o"])
        wt = w.WickTheorem(); wt.set_single_threaded(True)
        result = wt.contract(w.rational(1), T1@T2, 0, 0)
        assert len(result) == 0

    # ── coefficient consistency ───────────────────────────────────────────────

    def test_canon_on_total_coeff_equals_nocanon_total_for_one_body(self):
        """For the simplest case F@T1 contracted to rank 0, the sum of
        coefficients must agree between canon=ON and canon=OFF."""
        F  = w.utils.gen_op("f", 1, "ov", "ov")
        T1 = w.op("t", ["v+ o"])
        wt_on  = w.WickTheorem(); wt_on.set_single_threaded(True)
        wt_off = w.WickTheorem(); wt_off.set_single_threaded(True)
        wt_off.do_canonicalize_graph(False)
        r_on  = wt_on.contract(w.rational(1),  F@T1, 0, 0)
        r_off = wt_off.contract(w.rational(1), F@T1, 0, 0)
        # sum all coefficients
        def total(expr):
            s = w.rational(0)
            for _, c in expr:
                s = s + c
            return s
        assert total(r_on) == total(r_off)

    # ── sign tests ────────────────────────────────────────────────────────────

    def test_four_body_sign_stability(self):
        """T4 contracted with itself must have the same fermion sign
        structure regardless of canonicalization."""
        T4 = w.op("t", ["v+ v+ v+ v+ o o o o"])
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        # The expression is well-defined; verify canon/no-canon term counts match
        r_on  = contract(V@T4, canon=True)
        r_off = contract(V@T4, canon=False)
        assert len(r_on) <= len(r_off)
        assert len(r_on) > 0

    # ── high-symmetry: many identical operators ────────────────────────────────

    def test_triple_identical_operators(self):
        """V@T2@T2@T2: three identical T2 operators — maximum permutation
        degeneracy.  Canonicalization must reduce or preserve term count."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        n_on  = len(contract(V@T2@T2@T2, canon=True))
        n_off = len(contract(V@T2@T2@T2, canon=False))
        assert n_on <= n_off
        assert n_on > 0


