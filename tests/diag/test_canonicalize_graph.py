# ── Layer 2: mathematical invariants ─────────────────────────────────────────

class TestInvariantsOV:
    """Invariants that must hold regardless of canonicalization implementation."""

    @pytest.fixture(autouse=True)
    def _setup(self):
        setup_ov()

    def test_canon_reduces_or_preserves_term_count(self):
        """Canonicalization must never produce more terms than without it."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        T3 = w.op("t", ["v+ v+ v+ o o o"])
        T4 = w.op("t", ["v+ v+ v+ v+ o o o o"])
        for expr in [V@T2@T2, V@T3@T3, V@T4@T4, V@T3@T3@T2, V@T4@T2@T2]:
            n_on  = len(contract(expr, canon=True))
            n_off = len(contract(expr, canon=False))
            assert n_on <= n_off, (
                f"canon=True gave {n_on} terms but canon=False gave {n_off}"
            )

    def test_identical_operators_canon_never_adds_terms(self):
        """Canonicalization must never produce more terms than without it,
        even for products with repeated identical operators."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        T3 = w.op("t", ["v+ v+ v+ o o o"])
        T4 = w.op("t", ["v+ v+ v+ v+ o o o o"])
        for expr in [V@T2@T2, V@T3@T3, V@T4@T4]:
            n_on  = len(contract(expr, canon=True))
            n_off = len(contract(expr, canon=False))
            assert n_on <= n_off, (
                f"canon=True gave {n_on} terms but canon=False gave {n_off}"
            )

    def test_canon_off_terms_sum_to_canon_on(self):
        """Every canonical term's coefficient must equal the sum of the
        coefficients of all corresponding terms in the no-canon result.

        Both results go through term.canonicalize(), so tensor-structure
        strings are comparable.  We collect coefficients by term string and
        verify the two maps are equal.
        """
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        T3 = w.op("t", ["v+ v+ v+ o o o"])
        for expr in [V@T2, V@T3, V@T2@T2, V@T3@T3]:
            expr_on  = contract(expr, canon=True)
            expr_off = contract(expr, canon=False)
            # build coefficient maps keyed on string representation of each term
            map_on  = {str(t): c for t, c in expr_on}
            map_off: dict = collections.defaultdict(lambda: w.rational(0))
            for t, c in expr_off:
                map_off[str(t)] += c
            assert map_on == dict(map_off), (
                "canon=True and canon=False disagree on coefficients"
            )

    def test_empty_contraction_range_gives_empty_expression(self):
        """Contracting with minrank > maxrank must always return empty."""
        V  = w.utils.gen_op("v", 2, "ov", "ov")
        T2 = w.op("t", ["v+ v+ o o"])
        wt = w.WickTheorem(); wt.set_single_threaded(True)
        result = wt.contract(w.rational(1), V@T2, 10, 8)
        assert result == w.Expression()


