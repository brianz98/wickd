        T1b = w.utils.gen_op("t", 1, "bv", "cb", diagonal=False)
        r_a = contract(Fca@T1a)
        r_b = contract(Fcb@T1b)
        assert len(r_a) > 0 and len(r_b) > 0
        assert r_a != r_b

