"""
Tests for canonicalize_contraction_graph.

Three layers:
  Layer 1 – Regression: contracted expressions must exactly match .ref files
             generated by the reference implementation.
  Layer 2 – Invariants: properties that must hold regardless of implementation.
  Layer 3 – Canon-vs-no-canon consistency: every term produced without graph
             canonicalization must be accounted for in the canonical result.

Run with:
    pytest tests/diag/test_canonicalize_graph.py -v
"""

import collections
from pathlib import Path

import pytest
import wickd as w

THIS_DIR = Path(__file__).resolve().parent


# ── helpers ───────────────────────────────────────────────────────────────────

def load_ref(name: str) -> w.Expression:
    path = THIS_DIR / f"canon_{name}.ref"
    return w.utils.string_to_expr(path.read_text())


def contract(expr, *, canon: bool = True) -> w.Expression:
    wt = w.WickTheorem()
    wt.set_single_threaded(True)
    if not canon:
        wt.do_canonicalize_graph(False)
    return wt.contract(w.rational(1), expr, 0, 999)


def setup_ov():
    w.reset_space()
    w.add_space("o", "fermion", "occupied",   ["i", "j", "k", "l", "m", "n"])
    w.add_space("v", "fermion", "unoccupied", ["a", "b", "c", "d", "e", "f"])


def setup_cav():
    w.reset_space()
    w.add_space("c", "fermion", "occupied",   ["i", "j", "k", "l", "m", "n"])
    w.add_space("a", "fermion", "general",    ["u", "v", "w", "x", "y", "z"])
    w.add_space("v", "fermion", "unoccupied", ["a", "b", "c", "d", "e", "f"])


def setup_cabv():
    w.reset_space()
    w.add_space("c", "fermion", "occupied",   ["i", "j", "k", "l", "m", "n"])
    w.add_space("a", "fermion", "general",    ["u", "v", "w", "x", "y", "z"])
    w.add_space("b", "fermion", "general",    ["p", "q", "r", "s", "t", "U"])
    w.add_space("v", "fermion", "unoccupied", ["a", "b", "c", "d", "e", "f"])


